package com.ibm.as400.vaccess;

/**
  * This interface is used to hold the copyright string for our package.
  * All classes in the package need a copyright and should get that
  * by declaring a String constant as:
  * <PRE>
  *    static final private String x = Copyright_v.copyright;
  * </PRE>
  *
  *
**/

interface Copyright_v
{
    static final String copyright = "(C)Copyright IBM Corp. 1997, 1999";
}

