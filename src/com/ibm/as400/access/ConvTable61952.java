    byte[] stringToByteArray(String source)
    {
	char[] src = source.toCharArray();
	if (ConvTable.convDebug)
	{
	    Trace.log(Trace.CONVERSION, "Converting char to byte for CCSID: 61952", ConvTable.dumpCharArray(src));
	}

	byte[] dest = new byte[src.length*2];
	int destpos = 0;

	for (int srcpos = 0; srcpos < src.length; ++srcpos)
	{
            // get bytes for current character
	    char curchar = src[srcpos];
	    int byte1 = (curchar >>> 8) & 0xFF;
	    int byte2 =  curchar        & 0xFF;

            // see if first byte yields index into table
	    int index = stobIndexTable[byte1];
	    if (index == -1)
	    {
		// pass char through
		dest[destpos++] = (byte)byte1;
		dest[destpos++] = (byte)byte2;
	    }
	    else
	    {
		// get char from table
		char convChar = stobTable[index].charAt(byte2);
		dest[destpos++] = (byte)(convChar >>> 8);
		dest[destpos++] = (byte) convChar;
	    }
	}

	if (ConvTable.convDebug)
	{
	    Trace.log(Trace.CONVERSION, "Char to byte output: ", dest);
	}
	return dest;
    }
